void delay_us(uint32_t us) {
    uint32_t ticks = us * (SystemCoreClock / 1000000);
    uint32_t start = SysTick->VAL;

    // 如果SysTick倒计数寄存器的值大于要延迟的ticks，则说明已经过了1个SysTick的时间
    // 否则，循环等待直到延迟时间到达
    while ((start - SysTick->VAL) < ticks) {}
}
